#include <stdlib.h>

int main()
{
   int    score[5]; //宣告 score為 整數 型別陣列，陣列元素為 score[0]∼score[4] 
   char   name[20]; //宣告 name 為 字元 型別陣列，陣列元素為 name[0]∼name[19] 
   float  price[7]; //宣告 price為 浮點數 型別陣列，陣列元素為 price[0]∼price[6] 
   double sum[10];  //宣告 sum  為 倍精確數 型別陣列，陣列元素為 sum[0]∼sum[9] 
 
   printf("宣告陣列完成\n\n");

   system("pause"); 
   return 0;        
}
